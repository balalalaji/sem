	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x40001000
	DCD Reset_Handler
	ALIGN
	AREA mycode,CODE,READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=N1
	LDR R1,=N2
	LDR R2,=RES
	MOV R3,#4
	MOV R6,#0
UP	LDR R4,[R0],#4
	LDR R5,[R1],#4
	ADDS R7,R7,R5
	ADC R6,#0
	STR R7,[R2],#4
	BNE UP
STOP B STOP	
N1 DCD OxFFFFFFFF,3,2,1
N2 DCD OxFFFFFFFF,3,2,1	
	AREA mydata,DATA,READWRITE
RES DCD 0
	END